export const getPages = (): Page[] => {
  return initialPagesWithSections.map((item) => item.page);
};

export const getSections = (): Section[] => {
  return initialPagesWithSections.flatMap((item) => item.sections);
};

export const getSectionsByPageId = (pageId: string): Section[] => {
  const pageData = initialPagesWithSections.find((item) => item.page.id === pageId);
  return pageData ? pageData.sections : [];
};

export default initialPagesWithSections;
